package Week2.Day06_if_statements;

public class IfAndElseStatements {

    public static void main(String[] args) {

        int age = 20;

        if (age >= 21) {
            System.out.println("Eligible");
        }

        if (age < 21) {
            System.out.println("Not eligible");
        }
        System.out.println("-------------------");

        if (age >= 21) {
            System.out.println("Eligible");
        } else {
            System.out.println("Not eligible");

        }



    }
}