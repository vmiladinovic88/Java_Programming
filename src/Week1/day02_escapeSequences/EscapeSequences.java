package Week1.day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have Java class");
        System.out.println("_____________________________");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch 1");

        System.out.println("____________________________________");

        System.out.println("\\");

        System.out.println("____________________________________");

        System.out.println("My favorite TV show is \"The Walking dead\"");
    }

}

// single line comment


/*
    1. \n: New Line
    2. \t: paragraph space
    3. \\: back slash
    4. \": double quote
 */