package Week1.Day03_variables;

public class VariableNamingRules {

    public static void main(String[] args) {

        // age of the student is 28
        // salary is 10000

        int age = 28;
        int salary = 10000;

        // int age = 32; // Variable name must be unique

        int phoneNumber = 1712768028; //camel case

        double salaryBeforeTax = 100000.5;

        double salary_After_Tax$ = 80000.5; // underscore and dollar sign only

        System.out.println("___________________");

        int number1 = 10;
        int number2 = 20;
        int number3 = 30; // can not start with digits, it can include

        // double abstract = 5.5; // variable names can not be java reserved words
        // double final = 85.5;

        System.out.println("------------");

        // gender is male

        String gender = "male";

        String fullName = "Cydeo Schhol";

        System.out.println(gender);
        System.out.println(fullName);


    }

}
